import type { EvaluationSummary } from "../../../lib/evaluationTypes";

type EvaluationSummaryCardsProps = {
  summary: EvaluationSummary;
};

export function EvaluationSummaryCards({ summary }: EvaluationSummaryCardsProps) {
  const cards = [
    { label: "Fixtures", value: summary.fixture_ids.length.toString(), detail: summary.fixture_ids.join(", ") },
    { label: "Queries", value: summary.query_count.toString(), detail: "Seeded evaluation queries" },
    { label: "Candidates", value: summary.candidate_count.toString(), detail: "Across all fixture queries" },
    { label: "Judgments", value: summary.judgment_count.toString(), detail: "Explicit relevance labels" },
    { label: "Top-K", value: summary.top_ks.join(", "), detail: "Backend evaluation settings" },
    { label: "Source", value: summary.generated_from ?? "Backend evaluation", detail: "Generated by the 6D API" },
  ];

  return (
    <section aria-labelledby="evaluation-summary-title">
      <div className="flex flex-col gap-2 sm:flex-row sm:items-end sm:justify-between">
        <div>
          <p className="text-sm font-semibold uppercase tracking-wide text-accent">Dataset summary</p>
          <h2 id="evaluation-summary-title" className="mt-2 text-2xl font-bold tracking-normal text-ink">
            Seeded evaluation run
          </h2>
        </div>
      </div>

      <dl className="mt-5 grid gap-3 sm:grid-cols-2 lg:grid-cols-3">
        {cards.map((card) => (
          <div key={card.label} className="rounded-lg border border-line bg-white p-5 shadow-soft">
            <dt className="text-xs font-semibold uppercase tracking-wide text-muted">{card.label}</dt>
            <dd className="mt-2 break-words text-2xl font-bold tracking-normal text-ink">{card.value}</dd>
            <dd className="mt-2 text-sm leading-6 text-muted">{card.detail}</dd>
          </div>
        ))}
      </dl>
    </section>
  );
}
